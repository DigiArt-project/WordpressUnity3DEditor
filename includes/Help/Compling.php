<h2>Compiling and disseminating</h2>

<p class=MsoNormal style='margin-top:0in;margin-right:0in;margin-bottom:6.0pt;
margin-left:.75pt;text-align:justify;text-indent:-.75pt'>The compiling of the game project towards a binary or a web page is a single click procedure, though many things happen in the server that can last several minutes depending on the size of the Game Project and the computational power of the server. The compiling consists of three steps that are seamlessly executed in the server. First, the project is transformed from WordPress into Unity3D. Although this is simple to say, it is not straight-forward and most of the work in 2016 was dedicated in the theoretical framework establishment for this procedure. Second, the Game Project is compiled by Unity3D into a Game. Third the game is zipped in a single file.  </p>
    
<p>The Game Project can be compiled in the Project Editor page by clicking on the button “COMPILE GAME”    on the top right corner as shown in Figure 10.</p>
    
